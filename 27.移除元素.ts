// @lc code=start
function removeElement(nums: number[], val: number): number {
  if (!nums || nums.length === 0) {
    return 0;
  }

  let i: number = 0;
  for (let j = 0; j < nums.length; j++) {
    if (nums[j] !== val) {
      nums[i] = nums[j];
      i++;
    }
  }
  return i;
};
